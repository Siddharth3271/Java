public class Program18 {
    public static void main(String[] args) {
        int a=10;
        System.out.println("The integer value is "+a);
        double b=a;
        System.out.println("The double value is "+b);

        double num=10.999;
        System.out.println("The double value is "+num);

        int c=(int)num;
        System.out.println("The double to integer "+c);

        char d='a';
        int ans=(int)d;
        System.out.println("The integer value is "+ans);

    }
}
